dia = input("Me diga um valor entre 1 e 7: ")

if dia == '1':
    print("É domingo")
elif dia == '2':
    print("É segunda-feira")
elif dia == '3':
    print("É terça-feira")
elif dia == '4':
    print("É quarta-feira")
elif dia == '5':
    print("É quinta-feira")
elif dia == '6':
    print("É sexta-feira")
    print("Sextou")
elif dia == '7':
    print("Finalmente é sabado")
else:
    print('É X-feira')
